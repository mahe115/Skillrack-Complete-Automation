def select_until_end(driver, repeats=20):
    # Create an ActionChains instance
    actions = ActionChains(driver)

    # Perform the key combination multiple times
    for _ in range(repeats):
        actions.key_down(Keys.CONTROL).key_down(Keys.SHIFT).send_keys(Keys.ARROW_RIGHT).key_up(Keys.SHIFT).key_up(Keys.CONTROL).perform()
        time.sleep(0.05)  # Small delay to avoid overwhelming the system

    # Press DELETE to remove the selected text
    actions.send_keys(Keys.DELETE).perform()

    print("♻️ The auto-created braces and brackets generated by the editor are cleared.")


