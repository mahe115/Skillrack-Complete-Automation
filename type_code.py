def human_typing(driver,text, actions):
    try:
        text = "\n".join(line.lstrip() for line in text.splitlines())  
        typo_chance = 0.05  # 5% chance of a typo
        backspace_delay = random.uniform(0.15, 0.3)  # Delay before correcting typo

        for char in text:
            if random.random() < typo_chance and char.isalnum():  
                # Introduce a typo by sending a wrong character
                wrong_char = random.choice("abcdefghijklmnopqrstuvwxyz0123456789")
                actions.send_keys(wrong_char)
                actions.perform()
                time.sleep(backspace_delay)  # Simulate thinking time before correction

                # Simulate pressing backspace **only once** to remove the wrong character
                actions.send_keys(Keys.BACKSPACE)
                actions.perform()
                time.sleep(random.uniform(0.1, 0.2))  # Pause before retyping correctly

            # Type the correct character
            actions.send_keys(char)
            actions.perform()

            # Introduce a realistic typing delay
            base_delay = random.uniform(0.05, 0.2)  
            if random.random() < 0.1:  # 10% chance of a longer pause
                base_delay += random.uniform(0.3, 0.7)

            time.sleep(base_delay)

        #Select and remove extra braces after code typed successfully
        select_until_end(driver)
    
    # Exception handling for any interrupt while typing code in the editor
    except WebDriverException as e:
        print(f"❌ Error detected while typing as human typing: {e}")
        return

