def auto_type_rough_code(code,driver):
    """Clears the existing code, types the given code, clicks Run twice, then extracts the solution code."""
    print("🔃 Typing code into editor...")

    # Locate and activate the editor
    editor = WebDriverWait(driver, 10).until(
        EC.presence_of_element_located((By.CLASS_NAME, "ace_editor"))
    )
    driver.execute_script("arguments[0].scrollIntoView({block: 'center'});", editor)
    time.sleep(1)

    actions = ActionChains(driver)
    actions.move_to_element(editor).click().perform()
    time.sleep(1)

    # Ensure the editor is active
    if "ace_focus" not in editor.get_attribute("class"):
        print("❌ Editor not focused, retrying...")
        actions.move_to_element(editor).click().perform()
        time.sleep(1)

    # **Clear Existing Code using Ctrl + A & Delete**
    actions.key_down(Keys.CONTROL).send_keys("a").key_up(Keys.CONTROL).perform()
    time.sleep(0.2)
    actions.send_keys(Keys.DELETE).perform()
    time.sleep(0.5)

    # **Auto-Type Code One Character at a Time**
    for char in code:
        actions.send_keys(char)
        actions.perform()
        time.sleep(0.03)

    print("🎳 Rough Code typed successfully.")
    

def clicking_run_btn_twice(driver,run_button_id):
    
    # **Click Run Button Twice**
    for i in range(2):  # Click twice
        print(f"🔄 Clicking Run button ({i+1}/2)...")
        try:
            run_button = WebDriverWait(driver, 10).until(
                EC.element_to_be_clickable((By.ID, run_button_id))  # Corrected ID
            )
            driver.execute_script("arguments[0].scrollIntoView({block: 'center'});", run_button)
            time.sleep(0.5)
            run_button.click()
            print(f"🚀 Run button clicked successfully ({i+1}/2).")

            # Wait for compilation before second click
            if i == 0:
                print("⏳ Waiting for compilation...")
                time.sleep(5)  # Adjust based on compilation time
        except Exception as e:
            print(f"❌ Failed to click Run button: {e}")
            
